package KitEscolarEjercicio;

public class Deportivo {
    private int talla;
    private String color;
    private int prendas;

    public int getTalla() {
        return talla;
    }

    public void setTalla(int talla) {
        this.talla = talla;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getPrendas() {
        return prendas;
    }

    public void setPrendas(int prendas) {
        this.prendas = prendas;
    }
}
