package Practica3.ejercicio3;

public class Fritura {
	private String gaseosa;
	private String pipoca;
	private String chocolates;

	public String getGaseosa() {
		return gaseosa;
	}

	public void setGaseosa(String gaseosa) {
		this.gaseosa = gaseosa;
	}

	public String getPipoca() {
		return pipoca;
	}

	public void setPipoca(String pipoca) {
		this.pipoca = pipoca;
	}

	public String getChocolates() {
		return chocolates;
	}

	public void setChocolates(String chocolates) {
		this.chocolates = chocolates;
	}

}
